<p style="line-height: 1.15;"><span style="width:48px">&nbsp;</span><span style="font-size: 11pt;">Sistem Informasi management Koperasi Karyawan mencakup sistem inventory dan akuntansi. Integrasi antara transaksi penjualan dengan pembuatan jurnal di akuntansi.</span></p>
<p style="line-height: 1.15;"><span style="font-size: 11pt;">Sistem inventory dalam applikasi ini mencakup pengelolaan barang berdasarkan kategori dan jenis barang, pembelian barang tunai maupun konsinyasi, penjualan tunai dan kredit anggota koperasi,history stock berdasarkan kode barang maupun berdasarkan pemasok serta laporan pembelian maupun penjualan.</span></p>
