<p style="line-height: 1.15;"><span style="font-size: 11pt;">Sistem Akuntansi dalam applikasi ini mencakup pembuatan jurnal umum, buku besar, anggota koperasi, laporan keuangan anggota koperasi, pembuatan neraca, neraca lajur dan laporan SHU, pengaturan kode perkiraan , klasifikasi perkiraan serta grafik SHU dalam tahun. Laporan neraca dan SHU bisa dilakukan perbandingan dengan data sebelumnya yang dipilih</span></p>
<p style="line-height: 1.15;">&nbsp;</p>
<p style="line-height: 1.15;"><span style="font-size: 11pt;">Applikasi ini merupakan applikasi web base dengan bahasa pemograman PHP , Codeigniter, jQuery dan MySQL sebagai database nya. Bisa diakses dalam jaringan Intranet atau Internet dan menerapkan sistem Multiuser dengan hak akses masing - masing user bisa diatur secara terpisah berdasarkan group user tersebut.</span></p>
